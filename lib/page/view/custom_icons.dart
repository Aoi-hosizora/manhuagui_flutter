  final double? size;
  final Color? color;

  @override
  Widget build(BuildContext context) {
    assert(
      icon == null || icon is IconData || icon is PathIconData,
      'CustomIcon.icon should be null, or an IconData, or a PathIconData',
    );

    if (icon == null) {
      return Icon(null, size: size, color: color);
    }
